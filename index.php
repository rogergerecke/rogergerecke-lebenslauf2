<?php
// read given dir and return a file array
function _imageLinks($dir)
{
    if (is_dir($dir)) {
        if ($dh = opendir($dir)) {

            // set phat
            $phat = $dir;
            if (substr($phat, -1) != '/') {
                $phat = $phat . '/';
            }


            // read every file and set $file = filename.jpg
            while (false !== ($file = readdir($dh))) {
                if (is_file($phat . $file)) {

                    $size = getimagesize($phat . $file);
                    $files[] = [
                        'file_phat' => $phat,
                        'file_name' => $file,
                        'file_weight' => $size[1],
                        'file_height' => $size[0],
                    ];
                }
            }
            closedir($dh);
        }
    }

    return $files;
}

